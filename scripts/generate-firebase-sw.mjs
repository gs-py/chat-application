#!/usr/bin/env node
/**
 * Generates public/firebase-messaging-sw.js from env vars.
 * Run before dev/build if using Firebase push. Requires .env with VITE_FIREBASE_*.
 */
import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const envPath = resolve(root, '.env');
let env = {};
try {
  const content = readFileSync(envPath, 'utf8');
  content.split('\n').forEach((line) => {
    const m = line.match(/^([^#=]+)=(.*)$/);
    if (m) env[m[1].trim()] = m[2].replace(/^["']|["']$/g, '').trim();
  });
} catch {
  console.warn('No .env found, firebase-messaging-sw.js will have empty config');
}

const config = {
  apiKey: env.VITE_FIREBASE_API_KEY || 'AIzaSyBphYM2HL0VPAFdC8YUTeym5rLYHJkjvf8',
  authDomain: env.VITE_FIREBASE_AUTH_DOMAIN || 'chat-app-6f938.firebaseapp.com',
  projectId: env.VITE_FIREBASE_PROJECT_ID || 'chat-app-6f938',
  storageBucket: env.VITE_FIREBASE_STORAGE_BUCKET || 'chat-app-6f938.firebasestorage.app',
  messagingSenderId: env.VITE_FIREBASE_MESSAGING_SENDER_ID || '621218037610',
  appId: env.VITE_FIREBASE_APP_ID || '1:621218037610:web:21cf564f58f8cabb526f13',
};

const sw = `// Generated by scripts/generate-firebase-sw.mjs - do not edit manually
importScripts('https://www.gstatic.com/firebasejs/11.4.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/11.4.0/firebase-messaging-compat.js');

firebase.initializeApp(${JSON.stringify(config)});

const messaging = firebase.messaging();
messaging.onBackgroundMessage((payload) => {
  const title = payload.notification?.title || payload.data?.title || 'New message';
  const body = payload.notification?.body || payload.data?.body || '';
  const options = { body, icon: '/favicon.ico' };
  self.registration.showNotification(title, options);
});
`;

writeFileSync(resolve(root, 'public', 'firebase-messaging-sw.js'), sw);
console.log('Generated public/firebase-messaging-sw.js');